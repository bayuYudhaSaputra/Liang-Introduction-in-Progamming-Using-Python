# Konversi Mil ke Kilometer
# =========================================================

print(
    "=================================================== \n",
    "Konversi Mil ke Kilometer \n",
    "--------------------------------------------------- \n"
    )

print(
    " ========================= \n"
    " |", "No.","|", format("Mil","5s"), "|",format("Kg","5s"), "|", "\n",
    "-------------------------"
    )

for nomor in range(1,11):
    mil = nomor
    kilogram = 1.609 * mil
    print(
        " |",format(nomor,"3d"), "|",format(mil, "5.2f"), "|",format(kilogram, "5.2f"),"|"
        )
    
print("==========================")