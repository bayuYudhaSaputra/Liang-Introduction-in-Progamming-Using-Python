# Mengimport Turtle
import turtle

# Menentukan posisi awal garis
turtle.showturtle()

# Memunculkan Teks "Welcome to Python"
turtle.write("Welcome to Python")

# Menggerakkan garis ke kanan sejauh 100 piksel.
turtle.forward(100)

# Membelokkan garis 90 derajat ke kanan
turtle.right(90)

# Mengganti warna garis menjadi merah
turtle.color("red")

# Menggerakkan garis ke depan sejauh 50 piksel
turtle.forward(50)

# Membelokkan garis 90 derajat ke kanan
turtle.right(90)

# Mengganti warna garis menjadi hijau
turtle.color("green")

# Menggerakkan garis maju ke depan sejauh 100 piksel
turtle.forward(100)

# Membelokkan garis ke kanan sejauh 45 derajat
turtle.right(45)

# Menggerakkan garis maju ke depan sejauh 80 piksel
turtle.forward(80)