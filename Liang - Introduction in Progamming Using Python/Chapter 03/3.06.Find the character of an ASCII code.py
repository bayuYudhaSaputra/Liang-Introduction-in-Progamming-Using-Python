# =======================================================
# Mengubah kode ASCII menjadi karakter
# =======================================================

# 1. Input kode ASCII
kodeASCII = eval(input("Input kode ASCII : "))

# 2. Ubah kode ASCII menjadi karakter
karakterKonversi = chr(kodeASCII)

# 3. Tampilkan hasil pengubahan kode ASCII
print("Karakter untuk kode ASCII",
        kodeASCII,
        "adalah",
        karakterKonversi
)
