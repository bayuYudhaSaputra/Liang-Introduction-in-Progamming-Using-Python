# Konversi Radian ke Derajat
# =============================================

# Import modul math
import math

# definisikan sudut radian
sudutRadian = math.pi / 7

# konversi sudut radian ke sudut radian
sudutDerajat = math.degrees(sudutRadian)

# tampilkan hasil konversi
print("Pi / 7 radian = ", sudutDerajat, "derajat")