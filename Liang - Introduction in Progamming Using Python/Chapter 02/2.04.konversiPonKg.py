# Konversi Pon ke Kilogram
# ------------------------------------------------------------------------------------------------
# Langkah 1. input ukuran dalam satuan pon
# Langkah 2. Hitung ukuran tersebut dengan satuan kilogram dengan rumus : kilogram = 0.454 * pon
# Langkah 3. Tampilkan ukuran dengan satuan kilogram
# ------------------------------------------------------------------------------------------------

# Langkah 1. input ukuran dalam satuan pon
pon = eval(input("Input ukuran dengan satuan pon :"))

# Langkah 2. Hitung ukuran tersebut dengan satuan kilogram
kilogram = 0.454 * pon

# Langkah 3. Tampilkan ukuran dengan satuan kilogram
print(pon, "pon sama dengan", kilogram, "Kg")

