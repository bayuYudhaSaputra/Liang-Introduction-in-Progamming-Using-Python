# Konversi Celsius ke Fahrenheit
# -----------------------------------------------------------------------------------------
# Step 1. Mendefinisikan variabel celsius menggunakan user input.
# Step 2. Menghitung nilai fahrenheit dengan rumus fahrenheit = (9 / 5) * celsius + 32
# Step 3. Tampilkan nilai fahrenheit
# =========================================================================================

# Step 1
# --------
celsius = eval(input("Ketikkan suhu dalam Celsius :"))

# Step 2
# --------
fahrenheit = (9 / 5) * celsius + 32

# Step 3
# --------
print("Suhu", celsius, "derajat celsius sama dengan", fahrenheit, "derajat fahrenheit.")

