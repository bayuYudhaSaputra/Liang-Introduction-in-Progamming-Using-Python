# Langkah 1. Input panjang sisi segienam beraturan
panjangSisi = eval(input("Input panjang sisi segienam beraturan :"))

# Langkah 2. Hitung luas segienam beraturan dengan rumus :
luasSegienamBeraturan = (( 3 * 3 ** 0.5 ) / 2) * panjangSisi ** 2

# Langkah 3. Tampilkan luas segienam beraturan
print(
        "Luas segienam beraturan dengan panjang sisi",
        panjangSisi,
        "satuan panjang adalah :",
        luasSegienamBeraturan,
        "satuan luas"
    )

