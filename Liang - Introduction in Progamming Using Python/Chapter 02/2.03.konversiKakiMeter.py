# Konversi Satuan Kaki ke Meter
# ------------------------------------------------------------------------
# Langkah 1. Input ukuran dalam satuan kaki
# Langkah 2. Hitung ukuran dalam meter dengan rumus meter = 0.305 * kaki
# Langkah 3. Tampilkan ukuran dengan satuan meter
# ------------------------------------------------------------------------

# Langkah 1. Input ukuran dalam satuan kaki
kaki = eval(input("Input ukuran dalam satuan kaki :"))

# Langkah 2. Hitung ukuran dalam meter dengan rumus meter = 0.305 * kaki
meter = 0.305 * kaki

# Langkah 3. Tampilkan ukuran dengan satuan meter
print(kaki, "kaki sama dengan", meter, "meter")
